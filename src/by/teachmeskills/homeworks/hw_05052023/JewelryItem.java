package by.teachmeskills.homeworks.hw_05052023;

public class JewelryItem {
    private final String name;
    private final int cost;

    public JewelryItem(String name, int cost) {
        this.name = name;
        this.cost = cost;
    }

    public String getName() {
        return name;
    }

    public int cost() {
        return cost;
    }
}
